public class AutoGenerate {
    public static void main(String[] args) {
        String result ;
        char mode;
        switch (mode){
            case 'F':
                result = "115F1020001";
                break;
            case 'T':
                result = "115T2020002";
                break;
            case 'B':
                result = "115B3040003";
                break;
        }
    }
}
