public class Travel {
    protected char modelOfTravel;
    protected int noOfTickets;
    protected double amount;

    public char getModelOfTravel() {
        return modelOfTravel;
    }

    public void setModelOfTravel(char modelOfTravel) {
        this.modelOfTravel = modelOfTravel;
    }

    public int getNoOfTickets() {
        return noOfTickets;
    }

    public void setNoOfTickets(int noOfTickets) {
        this.noOfTickets = noOfTickets;
    }

    public double getAmount() {
        return amount;
    }

    public void setAmount(double amount) {
        this.amount = amount;
    }
}
